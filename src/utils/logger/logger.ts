export const log = (
	content: string,
	type: LoggerColor = LoggerColor.NONE,
	more?: {
		username?: string;
		error?: any;
	}
): void => {
	counter = counter === 20 ? 0 : ++counter;
	separator(counter);

	switch (type) {
		case LoggerColor.NONE:
			return console.log(`    â”ƒ [LOG]         â”ƒ ${content}`);
		case LoggerColor.READY:
			return console.log(`    â”ƒ ${black(bgGreen("[READY]"))}       â”ƒ âœ… ${green(content)} `);
		case LoggerColor.COMMAND:
			return console.log(
				`    â”ƒ ${black(bgCyanBright("[COMMAND]"))}     â”ƒ ğŸ“ ${cyanBright(
					content
				)} invoked by ${black(bgCyanBright(more?.username || "unknown"))}`
			);
		case LoggerColor.EVENT:
			return console.log(
				`    â”ƒ ${black(bgBlue("[EVENT]"))}       â”ƒ ğŸ‘‚ ${blue(content)} Registered!`
			);
		case LoggerColor.WARNING:
			return console.log(`    â”ƒ ${black(bgYellow("[WARNING]"))}     â”ƒ âš ï¸ ${yellow(content)}`);
		case LoggerColor.ERROR:
			if (more?.error) console.log(more.error);
			return console.log(`    â”ƒ ${black(bgRedBright("[ERROR]"))}       â”ƒ ğŸ’€ ${redBright(content)}`);

		default:
			throw new TypeError("Something went wrong in logger system :(");
	}
};
